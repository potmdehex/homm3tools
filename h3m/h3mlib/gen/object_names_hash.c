#define uchar unsigned char

int
object_names_hash(const uchar *key)
{
	int i;
	unsigned f0, f1, f2;
	const uchar *kp = key;

	for (i=-32, f0=f1=f2=0; *kp; ++kp) {
		if (*kp < 32 || *kp > 122)
			return -1;
		if (kp-key > 32)
			return -1;
		f0 += T0[i + *kp];
		f1 += T1[i + *kp];
		f2 += T2[i + *kp];
		i += 91;
	}

	if (kp-key < 4)
		return -1;

	f0 %= 2169;
	f1 %= 2169;
	f2 %= 2169;

	if (f1 == f0 && ++f1 >= 2169)
		f1 = 0;
	if (f2 == f0 && ++f2 >= 2169)
		f2 = 0;
	if (f1 == f2) {
		if (++f2 >= 2169)
			f2 = 0;
		if (f2 == f0 && ++f2 >= 2169)
			f2 = 0;
	}

	return g[f0] + g[f1] + g[f2];
}